public interface GameData {

    //-----------------------------------------------------------------
    //  Game status accessor.
    //-----------------------------------------------------------------
    String getStatus();

    //-----------------------------------------------------------------
    //  Mine accessor.
    //-----------------------------------------------------------------
    int getMines(int row, int col);

    //-----------------------------------------------------------------
    //  Tile accessor.
    //-----------------------------------------------------------------
    int getTiles(int row, int col);

    //-----------------------------------------------------------------
    //  Tile mutator.
    //-----------------------------------------------------------------
    void markTile(int row, int col, int tile);
}